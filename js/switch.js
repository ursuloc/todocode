var color = prompt("Ingrese un color primario: ");
switch (color) {
  case "rojo":
    console.log("Es rojo");
    break;

  case "amarillo":
    console.log("Es amarillo");
    break;

  case "azul":
    console.log("Es azul");
    break;

  default:
    console.log("El color ingresado no es un color primario");
}
